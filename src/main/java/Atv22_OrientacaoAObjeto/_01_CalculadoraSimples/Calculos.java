package Atv22_OrientacaoAObjeto._01_CalculadoraSimples;

public class Calculos {
    public double x;
    public double y;

    public double soma ()  {
        return x + y;
    }
    public double subtracao () {
        return x - y;
    }
    public double multiplicacao () {
        return x * y;
    }
    public double divisao () {
        return x / y;
    }
}
